import {Button} from "../components/Button";
import {Menu , X} from "lucide-react";
import { useEffect, useState } from "react";
const navLinks = [
{href: "#about" , label: "About"},
{href: "#projects" , label: "Projects"},
{href: "#testimonials" , label: "Tetimonials"},
{href:"#experience" , label:"Experiennce"}
]
export const Navbar = () =>
{
    const[isMobileMenuOpen , setIsMobileMenuOpen] = useState(false);
    const[isScrolled , setIsScrolled] = useState(false);
    useEffect(() =>
    {
        const handleScroll = () =>
        {
            setIsScrolled(window.scrollY > 50);
        };
        window.addEventListener("scroll" , handleScroll);
        return () => window.removeEventListener("scroll" , handleScroll);
    } , []);
    return(
   <header className={`fixed top-0 left-0 right-0 ${isScrolled ? "glass-strong py-3" : "bg-transparent py-5"} py-5 z-50`}>
    <nav className="container mx-auto px-6 md:flex items-center justify-between">
      <a href="#" className="text-xl font-bold tracking-tight hover:text-primary">
        Ramaka<span className="text-primary">.</span>
      </a>
      <div className="flex items-center gap-1">
        <div className="glass rounded-full px-2 py-1 items-center gap-1">
            {navLinks.map((link , index)=>
            (
            <a href={link.href} key={index} 
            classNmae="px-4 py-2 text-sm text-muted-foreground hover:text-foreground rounded-full hover:bg-surface">
                {link.label}
            </a>
            ))}
        </div>
      </div>
      <div className="hidden md:block">
        <Button size="sm">Contact Me</Button>
        </div>
        <div>
            <button 
            className="md:hidden p-2 text-foreground cursor-pointer animate-fade-in" 
            onClick={() => setIsMobileMenuOpen((prev) =>!prev)}>
         {isMobileMenuOpen ? <X/> : <Menu size={24} />}
            </button>
        </div>
    </nav>
    {/* Mobile Menu */}
    {isMobileMenuOpen && <div className="md:hidden glass-strong">
<div className="container mx-auto px-6 py-6 flex flex-col gap-4">
{navLinks.map((link , index) =>(
<a href={link.href} 
key={index}
onClick = {() => setIsMobileMenuOpen(false)}
 className="text-lg text-muted-foreground hover:text-foreground py-2">
    {link.label}
</a>
))}
<Button onClick={() => setIsMobileMenuOpen(false)}>Contact Me</Button>
</div>
</div>
}
   </header>
    )
}